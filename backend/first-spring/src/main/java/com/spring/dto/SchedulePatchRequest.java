package com.spring.dto;

import com.spring.Enums.ScheduleStatus;

import java.time.LocalDateTime;

public class SchedulePatchRequest {
    private Integer doctorId;
    private Integer machineId;      // send -1 or a wrapper to explicitly clear
    private Integer roomId;         // same
    private String procedureName;
    private String remarks;
    private String explainBy;
    private ScheduleStatus scheduleStatus;
    private LocalDateTime startDateTime;
    private LocalDateTime endDateTime;

    public Integer getDoctorId() {
        return doctorId;
    }

    public void setDoctorId(Integer doctorId) {
        this.doctorId = doctorId;
    }

    public Integer getMachineId() {
        return machineId;
    }

    public void setMachineId(Integer machineId) {
        this.machineId = machineId;
    }

    public Integer getRoomId() {
        return roomId;
    }

    public void setRoomId(Integer roomId) {
        this.roomId = roomId;
    }

    public String getProcedureName() {
        return procedureName;
    }

    public void setProcedureName(String procedureName) {
        this.procedureName = procedureName;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }

    public String getExplainBy() {
        return explainBy;
    }

    public void setExplainBy(String explainBy) {
        this.explainBy = explainBy;
    }

    public ScheduleStatus getScheduleStatus() {
        return scheduleStatus;
    }

    public void setScheduleStatus(ScheduleStatus scheduleStatus) {
        this.scheduleStatus = scheduleStatus;
    }

    public LocalDateTime getStartDateTime() {
        return startDateTime;
    }

    public void setStartDateTime(LocalDateTime startDateTime) {
        this.startDateTime = startDateTime;
    }

    public LocalDateTime getEndDateTime() {
        return endDateTime;
    }

    public void setEndDateTime(LocalDateTime endDateTime) {
        this.endDateTime = endDateTime;
    }
}
